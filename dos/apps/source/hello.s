; HELLO.S
; THE CLASSIC "HELLO WORLD" PROGRAM FOR DOS/281

.BANK 1
.TEXT
	; START HERE
	; SET OUT BANK TO BANK 0
START:	LOADI	A,0
	STORE	[DBANK],A
	
	; SET UP STACK
	LOADI	D,0X60-2
	
	; SET UP SYSTEM CALL ARGUMENTS
	LOADI	A,1
	STORE	[ARG_BNK],A
	LOADI	A,STRING
	LOADI	B,S_PUTS
	
	; SET UP RETURN ADDRESS
	LOADI	C,1	; RETURN BANK
	STOREF	[D+1],C
	LOADI	C,@+2	; RETURN ADDRESS
	
	; DO SYSTEM CALL
	JUMP	SYSCALL
	
	; AND EXIT
END:	LOADI	B,0
	JUMP	SYSJUMP
	


.DATA
.DEFL BYTE STRING	"HELLO, WORLD!",0X0A,0x0D,0
	