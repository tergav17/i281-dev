; XMODEM.S
; SEND OR RECIEVE A FILE VIA XMODEM

; BUFFER WILL START AT BANK 2
BUFFER	= 2

; BANK ALLOCATION STUFF
BI	= 1
BD	= 1

.TEXT
.BANK	BI
CORE0_B	= BI

	; START BY PROCESSING THE ARGUMENTS
START:	LOADI	A,0
	STORE	[DBANK],A
	
	; RESET FLAGS
	STORE	[SFLAG],A
	STORE	[TFLAG],A
	
	; SET UP STACK
	LOADI	D,0X60-2
	
	; SYSCALL RETURN BANK
	LOADI	C,1	
	STOREF	[D+1],C
	
	; GO TO ARGUMENT BANK
	LOAD	A,[CMDL_B]
	STORE	[ARG_BNK],A
	STORE	[DBANK],A
	
	; SET UP POINTER
	LOADI	C,0
	
	; SKIP THE INITIAL COMMAND
0:	LOADF	A,[C]
	LOADI	B,0X20
	CMP	A,B
	BRBE	1F
	ADDI	C,1
	JUMP	0B

	; LOOK FOR THE FIRST ARGUMENT
1:	LOADF	A,[C]
	ADDI	A,0
	BRZ	2F
	CMP	A,B
	BRA	EXECUTE
	ADDI	C,1
	JUMP	1B
	
	; NO ARGUMENT, ERROR
2:	LOADI	A,0
	STORE	[DBANK],A
	
	LOADI	A,STR_B
	STORE	[ARG_BNK],A
	LOADI	A,ERROR0
	
	; PRINT ERROR MESSAGE
	LOADI	B,S_PUTS
	LOADI	C,EXIT
	JUMP	SYSCALL

	
	; THERE IS AN ARG, TRY AND OPEN IT
EXECUTE:

	; EXIT PROGRAM
EXIT:	LOADI	B,0
	JUMP	SYSJUMP


	; BANK IS DONE, MOVE ON TO THE NEXT
BI	= BI+1
.TEXT
.BANK	BI
CORE1_B	= BI

.DATA
.BANK	BD
STR_B	= BD

	; ERROR MESSAGES
.DEFL BYTE ERROR0	"NO ARGUMENT",0X0A,0X0D,
			"USAGE: XMODEM [-ST] FILE",0X0A,0X0D,0
.DEFL BYTE ERROR1	"CAN'T OPEN FILE",0X0A,0X0D,0
.DEFL BYTE ERROR2	"CAN'T CREATE FILE",0X0A,0X0D,0
.DEFL BYTE ERROR3	"CAN'T CREATE FILE",0X0A,0X0D,0

	; ZERO BANK STUFF
.BANK	0
.BSS

	; COMMAND LINE ARGUMENTS
.DEFL BYTE SFLAG	0
.DEFL BYTE TFLAG	0